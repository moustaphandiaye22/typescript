// Explication : Définir un type directement, sans interface ni alias.
// Un type anonyme est un type qui n'a pas de nom explicite.
// Il est souvent utilisé pour des types simples ou pour des objets temporaires.
// Exemple d'utilisation d'un type anonyme en TypeScript
 let objetAnonyme: { nom: string; age: number } = {
    nom: "coach wane",
    age: 30
};
// Exemple d'utilisation du type anonyme
 console.log(objetAnonyme.nom); // Affiche "coach wane"
 console.log(objetAnonyme.age); // Affiche 30   