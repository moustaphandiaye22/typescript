// Explication : Chaîne de caractères typée.
// Le type string en TypeScript est utilisé pour représenter des séquences de caractères.
// Il est souvent utilisé pour stocker des textes, des messages, ou des données textuelles
let greeting: string = "Bonjour, TypeScript!"; // Déclaration d'une variable de type string
let farewell: string = "Au revoir, TypeScript!"; // Une autre variable de type string

// Exemple d'utilisation de la variable greeting
console.log(greeting); // Affiche "Bonjour, TypeScript!"
// Exemple d'utilisation de la variable farewell
console.log(farewell); // Affiche "Au revoir, TypeScript!"
// TypeScript permet de définir des types pour les variables, ce qui aide à détecter les erreurs de type lors de la compilation.    
// Cela permet de s'assurer que les valeurs assignées aux variables sont du type attendu.   
